        this.dialog = true
      },

      close () {
        this.dialog = false
        this.$nextTick(() => {
          this.editedItem = Object.assign({}, this.defaultItem)
          this.editedIndex = -1
        })
      },

      save () {
        if (this.editedIndex > -1) {
          Object.assign(this.Autor[this.editedIndex], this.editedItem)
        } else {
          this.Autor.push(this.editedItem)
        }
        this.close()
      },
